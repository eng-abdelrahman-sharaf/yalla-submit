-- Core entities
CREATE SEQUENCE STUDENTS_SEQ START WITH 1 INCREMENT BY 1 NOCACHE NOCYCLE;
CREATE SEQUENCE COHORTS_SEQ START WITH 1 INCREMENT BY 1 NOCACHE NOCYCLE;
CREATE SEQUENCE COURSES_SEQ START WITH 1 INCREMENT BY 1 NOCACHE NOCYCLE;
CREATE SEQUENCE COURSE_GROUPS_SEQ START WITH 1 INCREMENT BY 1 NOCACHE NOCYCLE;
CREATE SEQUENCE DELIVERABLES_SEQ START WITH 1 INCREMENT BY 1 NOCACHE NOCYCLE;
CREATE SEQUENCE NOTIFICATIONS_SEQ START WITH 1 INCREMENT BY 1 NOCACHE NOCYCLE;

CREATE TABLE STUDENTS (
    id          NUMBER DEFAULT STUDENTS_SEQ.NEXTVAL NOT NULL,
    name        VARCHAR2(255) NOT NULL,
    email       VARCHAR2(320) NOT NULL UNIQUE,
    is_admin    NUMBER(1)
                    DEFAULT 0
                    NOT NULL
                    CONSTRAINT STUDENTS_ADMIN_CHK
                        CHECK (is_admin IN (0,1)),
    created_at  TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT STUDENTS_PK PRIMARY KEY (id)
);

CREATE TABLE COHORTS (
    id          NUMBER DEFAULT COHORTS_SEQ.NEXTVAL NOT NULL,
    name        VARCHAR2(255) NOT NULL UNIQUE,
    created_at  TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT COHORTS_PK PRIMARY KEY (id)
);

CREATE TABLE COURSES (
    id          NUMBER DEFAULT COURSES_SEQ.NEXTVAL NOT NULL,
    name        VARCHAR2(255) NOT NULL UNIQUE,
    created_at  TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT COURSES_PK PRIMARY KEY (id)
);

CREATE TABLE COURSE_GROUPS (
    id          NUMBER DEFAULT COURSE_GROUPS_SEQ.NEXTVAL NOT NULL,
    course_id   NUMBER NOT NULL,
    name        VARCHAR2(255) NOT NULL,
    created_at  TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT COURSE_GROUPS_PK PRIMARY KEY (id),
    CONSTRAINT COURSE_GROUPS_COURSE_NAME_UK
        UNIQUE (course_id, name),
    CONSTRAINT COURSE_GROUPS_COURSE_FK
        FOREIGN KEY (course_id) REFERENCES COURSES(id)
        ON DELETE CASCADE
);

CREATE TABLE DELIVERABLES (
    id              NUMBER DEFAULT DELIVERABLES_SEQ.NEXTVAL NOT NULL,
    course_group_id NUMBER NOT NULL,
    name            VARCHAR2(255) NOT NULL,
    deadline        TIMESTAMP NOT NULL,
    created_at      TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT DELIVERABLES_PK PRIMARY KEY (id),
    CONSTRAINT DELIVERABLES_COURSE_GROUP_NAME_UK
        UNIQUE (course_group_id, name),
    CONSTRAINT DELIVERABLES_COURSE_GROUP_FK
        FOREIGN KEY (course_group_id) REFERENCES COURSE_GROUPS(id)
        ON DELETE CASCADE
);

CREATE TABLE NOTIFICATIONS (
    id             NUMBER DEFAULT NOTIFICATIONS_SEQ.NEXTVAL NOT NULL,
    deliverable_id NUMBER,
    course_id      NUMBER,
    title          VARCHAR2(255) NOT NULL,
    message        VARCHAR2(4000),
    created_at     TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT NOTIFICATIONS_PK PRIMARY KEY (id),
    CONSTRAINT NOTIFICATIONS_DELIVERABLE_FK
        FOREIGN KEY (deliverable_id) REFERENCES DELIVERABLES(id)
        ON DELETE CASCADE,
    CONSTRAINT NOTIFICATIONS_COURSE_FK
        FOREIGN KEY (course_id) REFERENCES COURSES(id)
        ON DELETE CASCADE,
    CONSTRAINT NOTIFICATIONS_TARGET_CHK
        CHECK (deliverable_id IS NOT NULL OR course_id IS NOT NULL)
);

-- Many-to-many junction tables
CREATE TABLE STUDENTS_COHORTS (
    student_id  NUMBER NOT NULL,
    cohort_id   NUMBER NOT NULL,
    enrolled_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT STUDENTS_COHORTS_PK PRIMARY KEY (student_id, cohort_id),
    CONSTRAINT STUDENTS_COHORTS_STUDENT_FK
        FOREIGN KEY (student_id) REFERENCES STUDENTS(id)
        ON DELETE CASCADE,
    CONSTRAINT STUDENTS_COHORTS_COHORT_FK
        FOREIGN KEY (cohort_id) REFERENCES COHORTS(id)
        ON DELETE CASCADE
);

CREATE TABLE COHORTS_COURSES (
    cohort_id   NUMBER NOT NULL,
    course_id   NUMBER NOT NULL,
    added_at    TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT COHORTS_COURSES_PK PRIMARY KEY (cohort_id, course_id),
    CONSTRAINT COHORTS_COURSES_COHORT_FK
        FOREIGN KEY (cohort_id) REFERENCES COHORTS(id)
        ON DELETE CASCADE,
    CONSTRAINT COHORTS_COURSES_COURSE_FK
        FOREIGN KEY (course_id) REFERENCES COURSES(id)
        ON DELETE CASCADE
);

CREATE TABLE STUDENTS_COURSE_GROUPS (
    student_id       NUMBER NOT NULL,
    course_group_id  NUMBER NOT NULL,
    followed_at      TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT STUDENTS_COURSE_GROUPS_PK PRIMARY KEY (student_id, course_group_id),
    CONSTRAINT STUDENTS_COURSE_GROUPS_STUDENT_FK
        FOREIGN KEY (student_id) REFERENCES STUDENTS(id)
        ON DELETE CASCADE,
    CONSTRAINT STUDENTS_COURSE_GROUPS_COURSE_GROUP_FK
        FOREIGN KEY (course_group_id) REFERENCES COURSE_GROUPS(id)
        ON DELETE CASCADE
);

CREATE TABLE STUDENTS_NOTIFICATIONS (
    student_id      NUMBER NOT NULL,
    notification_id NUMBER NOT NULL,
    is_read         NUMBER(1)
                        DEFAULT 0
                        NOT NULL
                        CONSTRAINT STUDENTS_NOTIFICATIONS_IS_READ_CHK
                            CHECK (is_read IN (0,1)),
    read_at         TIMESTAMP,
    CONSTRAINT STUDENTS_NOTIFICATIONS_PK PRIMARY KEY (student_id, notification_id),
    CONSTRAINT STUDENTS_NOTIFICATIONS_STUDENT_FK
        FOREIGN KEY (student_id) REFERENCES STUDENTS(id)
        ON DELETE CASCADE,
    CONSTRAINT STUDENTS_NOTIFICATIONS_NOTIFICATION_FK
        FOREIGN KEY (notification_id) REFERENCES NOTIFICATIONS(id)
        ON DELETE CASCADE
);

