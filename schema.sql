CREATE OR REPLACE VIEW VW_STUDENT_COURSE_GROUPS AS
SELECT
    SCG.STUDENT_ID,
    S.NAME AS STUDENT_NAME,
    CG.ID AS COURSE_GROUP_ID,
    CG.NAME AS COURSE_GROUP_NAME,
    C.ID AS COURSE_ID,
    C.NAME AS COURSE_NAME,
    SCG.FOLLOWED_AT
FROM STUDENTS_COURSE_GROUPS SCG
JOIN STUDENTS S ON S.ID = SCG.STUDENT_ID
JOIN COURSE_GROUPS CG ON CG.ID = SCG.COURSE_GROUP_ID
JOIN COURSES C ON C.ID = CG.COURSE_ID;

CREATE OR REPLACE VIEW VW_DELIVERABLE_DETAILS AS
SELECT
    D.ID AS DELIVERABLE_ID,
    D.NAME AS DELIVERABLE_NAME,
    D.DEADLINE,
    CG.ID AS COURSE_GROUP_ID,
    CG.NAME AS COURSE_GROUP_NAME,
    C.ID AS COURSE_ID,
    C.NAME AS COURSE_NAME
FROM DELIVERABLES D
JOIN COURSE_GROUPS CG ON CG.ID = D.COURSE_GROUP_ID
JOIN COURSES C ON C.ID = CG.COURSE_ID;

CREATE OR REPLACE VIEW VW_STUDENT_NOTIFICATIONS AS
SELECT
    SN.STUDENT_ID,
    N.ID AS NOTIFICATION_ID,
    N.TITLE,
    N.MESSAGE,
    N.COURSE_ID,
    N.DELIVERABLE_ID,
    SN.IS_READ,
    SN.READ_AT,
    N.CREATED_AT
FROM STUDENTS_NOTIFICATIONS SN
JOIN NOTIFICATIONS N ON N.ID = SN.NOTIFICATION_ID;
